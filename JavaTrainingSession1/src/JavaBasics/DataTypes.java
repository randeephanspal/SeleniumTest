package JavaBasics;

public class DataTypes {

	public static void main(String[] args) {
		
		// primitive datatype: int,double,char,boolean
		// 1. int
		int i=10;
		i=20;
		int j=30;
		System.out.println(i+j);
		int k=-10;
		int p=0;
		
		// 2. double
		double d=12.33;
		double d2=100; //100.00
		System.out.println(d2);
		
		// 3. char, single digit value written in single quotes
		char a='i';
		char c1='A';
		char c2='0';
		char c3='$';
		System.out.println(c3);
		
		// 4. boolean   (Boolean are values and keywords)
		boolean b1=true;
		boolean b2=false;
		System.out.println(b2);
		System.out.println(b1);
		
		// 5. string  (It is not a datatype, it is a class)
		String s = "Hello World";
		String s1="Selenium";
		System.out.println(s1);				
	}

}
